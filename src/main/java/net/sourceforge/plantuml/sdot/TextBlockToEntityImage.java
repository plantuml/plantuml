package net.sourceforge.plantuml.sdot;

import java.util.Objects;

import net.sourceforge.plantuml.klimt.color.HColor;
import net.sourceforge.plantuml.klimt.drawing.UGraphic;
import net.sourceforge.plantuml.klimt.font.StringBounder;
import net.sourceforge.plantuml.klimt.geom.XDimension2D;
import net.sourceforge.plantuml.klimt.shape.TextBlock;
import net.sourceforge.plantuml.svek.IEntityImage;
import net.sourceforge.plantuml.svek.Margins;
import net.sourceforge.plantuml.svek.ShapeType;

// Adapts a plain TextBlock (typically a fully laid-out Smetana Drawing) into an
// IEntityImage, so that a nested Smetana layout can be used as a leaf image.
public final class TextBlockToEntityImage implements IEntityImage {

	private final TextBlock textBlock;

	public TextBlockToEntityImage(TextBlock textBlock) {
		this.textBlock = Objects.requireNonNull(textBlock);
	}

	public void drawU(UGraphic ug) {
		textBlock.drawU(ug);
	}

	public XDimension2D calculateDimension(StringBounder stringBounder) {
		return textBlock.calculateDimension(stringBounder);
	}

	public HColor getBackcolor() {
		return textBlock.getBackcolor();
	}

	public ShapeType getShapeType() {
		return ShapeType.RECTANGLE;
	}

	public Margins getShield(StringBounder stringBounder) {
		return Margins.NONE;
	}

	public double getOverscanX(StringBounder stringBounder) {
		return 0;
	}

	public boolean isHidden() {
		return false;
	}

}
